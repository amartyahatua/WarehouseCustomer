package wasdev.sample;

public class History {
	
	private String hcid;
	private String hcdid;
	private String hcwid;
	private String hdid;
	private String hwid;
	private String hdate;
	private String hamount;
	private String hdata;
	
	public String getHcid() {
		return hcid;
	}
	public void setHcid(String hcid) {
		this.hcid = hcid;
	}
	public String getHcdid() {
		return hcdid;
	}
	public void setHcdid(String hcdid) {
		this.hcdid = hcdid;
	}
	public String getHcwid() {
		return hcwid;
	}
	public void setHcwid(String hcwid) {
		this.hcwid = hcwid;
	}
	public String getHdid() {
		return hdid;
	}
	public void setHdid(String hdid) {
		this.hdid = hdid;
	}
	public String getHwid() {
		return hwid;
	}
	public void setHwid(String hwid) {
		this.hwid = hwid;
	}
	public String getHdate() {
		return hdate;
	}
	public void setHdate(String hdate) {
		this.hdate = hdate;
	}
	public String getHamount() {
		return hamount;
	}
	public void setHamount(String hamount) {
		this.hamount = hamount;
	}
	public String getHdata() {
		return hdata;
	}
	public void setHdata(String hdata) {
		this.hdata = hdata;
	}
	
}
